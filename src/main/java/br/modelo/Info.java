package br.modelo;

public class Info {
	private int saida;
	private int entrada;
	private int numhospedados;
	private int reservas;
	private int quartosdisponiveis;
	private int lembretes;
	
	private String data;
	
	
	public int getSaida() {
		return saida;
	}
	public void setSaida(int saida) {
		this.saida = saida;
	}
	public int getEntrada() {
		return entrada;
	}
	public void setEntrada(int entrada) {
		this.entrada = entrada;
	}
	public int getNumhospedados() {
		return numhospedados;
	}
	public void setNumhospedados(int numhospedados) {
		this.numhospedados = numhospedados;
	}
	public int getReservas() {
		return reservas;
	}
	public void setReservas(int reservas) {
		this.reservas = reservas;
	}
	public int getQuartosdisponiveis() {
		return quartosdisponiveis;
	}
	public void setQuartosdisponiveis(int quartosdisponiveis) {
		this.quartosdisponiveis = quartosdisponiveis;
	}
	public int getLembretes() {
		return lembretes;
	}
	public void setLembretes(int lembretes) {
		this.lembretes = lembretes;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	
	
	
	
	
}
