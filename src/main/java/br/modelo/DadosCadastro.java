package br.modelo;

public class DadosCadastro {
	
	private String nome;
	private String cnpj;
	private String endereço;
	private String cidade;
	private String email;
	private String responsave;
	private String sennha;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getCnpj() {
		return cnpj;
	}
	public void setCnpj(String cnpj) {
		this.cnpj = cnpj;
	}
	public String getEndereço() {
		return endereço;
	}
	public void setEndereço(String endereço) {
		this.endereço = endereço;
	}
	public String getCidade() {
		return cidade;
	}
	public void setCidade(String cidade) {
		this.cidade = cidade;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getResponsave() {
		return responsave;
	}
	public void setResponsave(String responsave) {
		this.responsave = responsave;
	}
	public String getSennha() {
		return sennha;
	}
	public void setSennha(String sennha) {
		this.sennha = sennha;
	}
	
}
